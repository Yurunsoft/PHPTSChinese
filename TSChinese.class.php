	/**
	 * 繁体转简体
	 * @param string $text 
	 * @return string 
	 */
	public static function t2s($text)
	{
		$length = mb_strlen($text);
		$result = '';
		for($i=0; $i<$length; ++$i)
		{
			$char = mb_substr($text, $i, 1);
			if(isset(self::$dict[$char]))
			{
				$result .= self::$dict[$char];
			}
			else
			{
				$result .= $char;
			}
		}
		return $result;
	}

	/**
	 * 简体转繁体
	 * @param string $text 
	 * @return string 
	 */
	public static function s2t($text)
	{
		$length = mb_strlen($text);
		$result = '';
		for($i=0; $i<$length; ++$i)
		{
			$char = mb_substr($text, $i, 1);
			$key = array_search($char, self::$dict);
			if(false === $key)
			{
				$result .= $char;
			}
			else
			{
				$result .= $key;
			}
		}
		return $result;
	}
